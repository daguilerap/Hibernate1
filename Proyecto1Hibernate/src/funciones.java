public class funciones {
    Long co;
    Double med;

    public funciones(Long co, Double med) {
        this.co = co;
        this.med = med;
    }
    public funciones() {

    }


    public Long getCo() {
        return co;
    }

    public void setCo(Long co) {
        this.co = co;
    }

    public Double getMed() {
        return med;
    }

    public void setMed(Double med) {
        this.med = med;
    }
}
